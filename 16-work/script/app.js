let day = prompt("Hafta kununi kirgizing:")

if(day == 1){
    document.write("Monday");
}
else if(day==2) {
    document.write("Tuesday")
}
else if(day==3) {
    document.write("Wednesday")
}
else if(day==4) {
    document.write("Thursday")
}
else if(day==5) {
    document.write("Friday")
}
else if(day==6) {
    document.write("Saturday")
}
else if(day==7) {
    document.write("Sunday")
}
else{
    document.write("Bunaqa hafta kuni yoq")
}